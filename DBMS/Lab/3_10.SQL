--3.10->WRITE A TRIGGER TO UPDATE THE STATUS TABLE WHEN EMPLOYEE TUPLE IS INSERTED.
CREATE OR REPLACE TRIGGER CS10 AFTER INSERT ON EMPLOYEE FOR EACH ROW
DECLARE
NAM STATUS.NAME%TYPE;
BEGIN
SELECT NAME INTO NAM FROM DEPARTMENT WHERE DEPT_NO=:NEW.DEPARTMENT;
UPDATE STATUS SET NO_OF_EMP=NO_OF_EMP+1, TOTAL_SALARY=TOTAL_SALARY+:NEW.SALARY WHERE NAME=NAM;
END;
