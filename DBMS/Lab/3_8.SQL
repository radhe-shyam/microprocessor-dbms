--3.8->WRITE A FUNCTION TO RETURN THE NUMBER OF EMPLOYEES WORKING IN THE DEPARTMENT WHICH IS MAKING THE HIGHEST REVENUE FROM THE SPONSORED PROJECTS.
CREATE OR REPLACE FUNCTION CS8 RETURN NUMBER IS
N NUMBER(3);
BEGIN
SELECT COUNT(*) INTO N FROM EMPLOYEE WHERE DEPARTMENT IN (SELECT CONTROLLING_DEPARTMENT FROM PROJECT GROUP BY CONTROLLING_DEPARTMENT HAVING SUM(BUDGET)=(SELECT MAX(SUM(BUDGET)) FROM PROJECT GROUP BY CONTROLLING_DEPARTMENT ));
RETURN N;
END;




